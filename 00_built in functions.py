import math

# Built-in modules
# Functions
a = "This is my string for built-in functions"
print("length =", len(a))
print("type =", type(a))
print(a)

# class Str
a = "This is my string for string class functions"
print("upper:",a.upper())
print("",a.replace("my", "your"))

# Class int
b = 10
print("byte", b.to_bytes())
print("bit_length", b.bit_length())


# built-in Math
c = -10.1456
print("abs:",abs(c))
print("round:",round(c,2))

# Math Module 
print("floor:",math.floor(c))
